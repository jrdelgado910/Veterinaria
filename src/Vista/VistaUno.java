    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
         String tipo=(String) jComboBox1.getSelectedItem();
        if(tipo.equals("Canino")){
            jComboBox2.removeAllItems();
            jLabel7.setText("Nivel Entrenamiento:");
            jLabel7.setVisible(true);
            jComboBox2.addItem("0-4");
            jComboBox2.addItem("5-7");
            jComboBox2.addItem("8-10");
            jComboBox2.setVisible(true);
        }else if(tipo.equals("Felino")){
            jComboBox2.removeAllItems();
             jLabel7.setText("Taxoplasmosis:");
             jLabel7.setVisible(true);
             jComboBox2.addItem("Si");
             jComboBox2.addItem("No");
             jComboBox2.setVisible(true);
        }
        
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    private void jTextField4KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyTyped
        char validar=evt.getKeyChar();
        if(Character.isLetter(validar)){
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(null, "Ingresar solo numeros");
        }
        
    }//GEN-LAST:event_jTextField4KeyTyped

